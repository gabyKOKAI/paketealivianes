			carreras pudieran participar en el programa de Paketealivianes, realizando su Servicio Social, lo cual permitió que la atención del programa creciera 
			de unas horas a la semana a una atención diaria por varias horas, de esta manera se cubrienron mejor las necesidades del programa. </p>

			<br/>
			<p id="plaintext" class="text-justify">El éxito del programa ha permitido que el mismo pueda ser aplicado en otras áreas del Instituto Nacional de 
